package com.inter.vo;

public class CountVo {
	
	private Integer multipleCount;
	private Integer qaaCount;
	private Integer singleCount;
	private Integer upperCount;
	
	public Integer getMultipleCount() {
		return multipleCount;
	}
	public void setMultipleCount(Integer multipleCount) {
		this.multipleCount = multipleCount;
	}
	public Integer getQaaCount() {
		return qaaCount;
	}
	public void setQaaCount(Integer qaaCount) {
		this.qaaCount = qaaCount;
	}
	public Integer getSingleCount() {
		return singleCount;
	}
	public void setSingleCount(Integer singleCount) {
		this.singleCount = singleCount;
	}
	public Integer getUpperCount() {
		return upperCount;
	}
	public void setUpperCount(Integer upperCount) {
		this.upperCount = upperCount;
	}
}
